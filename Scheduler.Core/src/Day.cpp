#include "Day.hpp"

/*
 * ---------------------------------------------------------------------------------------------------- 
 * Day
 *   Summary: Named collection of hours.
 * ---------------------------------------------------------------------------------------------------- 
 */

namespace Scheduler {
    namespace Core {

        /*
         * ---------------------------------------------------------------------------------------- 
         * Day Constructors
         * ---------------------------------------------------------------------------------------- 
         */
        Day::Day() {}
        Day::Day(const std::string day_name) : name(day_name) {
            // Initialize the 24 hours.
            for (int i = 0; i < 24; i++) {
                hours[i] = Hour();
            }
        }

        /*
         * ---------------------------------------------------------------------------------------- 
         * Day Deconstructors
         * ---------------------------------------------------------------------------------------- 
         */
        Day::~Day() {}
    }
}
